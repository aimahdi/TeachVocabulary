package com.aimtechltd.teachvocabulary;

public class Word {

    private String word, meaning, synonyms, example;

    public Word() {
    }

    public String getWord() {
        return word;
    }


    public String getMeaning() {
        return meaning;
    }


    public String getSynonyms() {
        return synonyms;
    }


    public String getExample() {
        return example;
    }

}
